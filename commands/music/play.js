const { QueryType } = require('discord-player');

module.exports = {
    name: 'play',
    aliases: ['p'],
    utilisation: '{prefix}play [song name/URL]',
    voiceChannel: true,

    async execute(client, message, args) {
        if (!args[0]) return message.channel.send(`Gelieve een geldig verzoek sturen. ${message.author}... probeer het opnieuw! âŒ`);

        const res = await player.search(args.join(' '), {
            requestedBy: message.member,
            searchEngine: QueryType.AUTO
        });

        if (!res || !res.tracks.length) return message.channel.send(`Er zijn helaas geen resultaten gevonden! ${message.author}... probeer het opnieuw! âŒ`);

        const queue = await player.createQueue(message.guild, {
            metadata: message.channel
        });

        try {
            if (!queue.connection) await queue.connect(message.member.voice.channel);
        } catch {
            await player.deleteQueue(message.guild.id);
            return message.channel.send(`Ik kan dit kanaal niet joinen kijk naar mijn permissies! ${message.author}... probeer het opnieuw! âŒ`);
        }

        await message.channel.send(`Uw tracks laden! ${res.playlist ? 'playlist' : 'track'}... ğŸ§`);

        res.playlist ? queue.addTracks(res.tracks) : queue.addTrack(res.tracks[0]);

        if (!queue.playing) await queue.play();
    },
};